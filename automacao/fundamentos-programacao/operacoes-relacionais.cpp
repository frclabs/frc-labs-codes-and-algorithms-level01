#include <iostream>
using namespace std;

int main() 
{
    // 5 é maior que 4?
    cout << (5 > 4) << endl;  // true
    // 10 é igual a 10?
    cout << (10 == 10) << endl;  // true
    // Verdade é igual a falso?
    cout << (true == false) << endl;  // false
    // (4 é maior ou igual a tres?) 
    // A resposta do que esta dentro do parenteses é
    // igual a verdadeiro?
    cout << ((4 >= 3) == true) << endl;  // true

    return 0;
}


