public class Exemplo {
    public static void main(String args[]) {
        // Vamos ver se um numero é par...
        int num = 10;
        if ((num % 2) == 0) {
            // Se o resto da divisão por 2 for zero
            System.out.println("Esse número é par");
        } else {
            System.out.println("Esse número é impar");
        }
    }
}
