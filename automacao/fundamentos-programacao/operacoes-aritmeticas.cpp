#include <iostream>
using namespace std;

int main()
{
	// Uma simples adição
	cout << 3 + 5 << endl;
	// Uma simples subtração
	cout << 3 - 5 << endl;
	// Para dividir, usamos a barra "/"
	cout << 7 / 2 << endl;
	// Multiplicação fazemos com "*". Também podemos
	// utilizar parenteses para especificar o que será
	// calculado antes, igual na matemática
	cout << 10 * (5 + 4) << endl;

	return 0;
}
