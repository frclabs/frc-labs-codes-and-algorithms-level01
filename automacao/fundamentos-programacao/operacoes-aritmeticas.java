public class HelloWorldApp {
	public static void main(String args[]) {
		// Uma simples adicao
		System.out.println(3 + 5);
		// Uma simples subtracao
		System.out.println(3 - 5);
		// Para dividir, usamos a barra "/"
		System.out.println(7 / 2);
		// Multiplicacao fazemos com "*". Tambem
		// podemos utilizar parenteses para especificar
		// o que sera calculado antes, igual na matematica
		System.out.println(10 * (5 + 4));
	}
}
