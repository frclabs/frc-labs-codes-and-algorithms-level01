package edu.wpi.first.wpilibj.templates.timed;

import edu.wpi.first.wpilibj.TimedRobot;

/**
 * A Máquina Virtual do Java irá executar automaticamente as 
 * funções declaradas nesta classe.
 */
public class Robot extends TimedRobot {

  /**
   * Esta função é executada quando o robô é iniciado pela 
   * primeira vez e deve ser utilizada para códigos de
   * configuração inicial.
   */
  @Override
  public void robotInit() {
  }

  /**
   * Esta função é calculada periodicamente em intervalos 
   * específicos, não  importando o modo de operação atual
   * (autônomo, periódico ou teste).
   * Esse código também é executado quando o robô está 
   * desabilitado.
   */
  @Override
  public void robotPeriodic() {
  }

  /**
   * Esta função é executada no momento da inicialização do modo 
   * autônomo.
   */
  @Override
  public void autonomousInit() {
  }

  /**
   * Esta função será chamada periodicamente durante o modo 
   * autônomo.
   */
  @Override
  public void autonomousPeriodic() {
  }

  /**
   * Esta função é executada no momento da inicialização do modo 
   * teleoperado.
   */
  @Override
  public void teleopInit() {
  }

  /**
   * Esta função será chamada periodicamente durante o modo 
   * teleoperado.
   */
  @Override
  public void teleopPeriodic() {
  }

  /**
   * Esta função é executada no momento da inicialização do modo 
   * teste.
   */
  @Override
  public void testInit() {
  }

  /**
   * Esta função será chamada periodicamente durante o modo 
   * teste.
   */
  @Override
  public void testPeriodic() {
  }
}
